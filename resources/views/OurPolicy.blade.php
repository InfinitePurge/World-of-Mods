	<p>World Of Mods Policies and Guidelines 

In order to maintain a healthy environment that promotes both collaboration and accountability among our community, the following guidelines describe the acceptable use of World Of Mods file, image, and video sharing systems. 

  

Submitted files (mods, images, etc.)  and their associated content (names, descriptions, etc.) must adhere to the standards described in this document. Submitted files and any associated content will be reviewed by staff. 

  

Usage and Accreditation 

User submissions which include copyrighted content, be it game files or images, are prohibited unless proper permission has been granted by the property owner or appropriate representation. Such content may be removed from World Of Mods and services. 

 

Submissions must conform to the following guidelines: 

Submission of existing user-submitted content without obtaining permission from the original author(s) of said content is strictly prohibited. 

 

Proper credit to the original author(s) must be given unless the author(s) has/have specified that credit is not necessary. Please, be aware that the mere accreditation of an author is no substitute for receiving explicit permission to upload or modify someone else’s content. 

 

The uploader is responsible for understanding the legalities involved when using copyrighted work and submissions that contain such content may be removed. 

 

Compilations must provide functionality beyond repackaging. In other words, an acceptable compilation must be "more than the sum of its parts". 

 

All files submitted must be intended for sharing publicly. You must not use our services for your own personal storage or share files with a limited group of users. 

 

Language translations should be submitted only as an addition that works in conjunction with the original file and you should link to the original file as a mirror or in your file description. Translations that include content from the original mod without permission may be subject to moderation and will be removed if requested by the original file author(s). 

 

User-submitted content that is predominantly intended to interact with existing user-submitted content is subject to the approval of all parties involved and may be removed at the request of the author of the original content. 

 

We encourage all copyright holders to report unauthorised use of their property. If you are the copyright holder, you are free to inform us of a copyright violation by contacting supportwom@gmail.com 

 

Inappropriate Content 

As we serve a diverse audience, user-submitted content needs to be suitable for all audiences, or, alternatively, be appropriately tagged as adult content - where applicable. 

Please note that there are particular rules in place regarding the public image share. While nudity is acceptable in both the supporter image share, as well as in images which are part of an adult-tagged mod, nudity is not acceptable in the regular image share. 

Inappropriate Content Guidelines at a glance: 

Content that may be generally construed as provocative, divisive, objectionable, discriminatory, or abusive toward any real-world individual or group, may be subject to moderation. This includes but is not limited to content involving politics, race, religion, gender identity, sexuality, or social class. We tolerate content related to real world issues and events as long as the appropriate tag ("Real World Issues") is used and the content is handled in a tasteful, respectful, and non-inflammatory manner. 

 

Content related to the World War II Axis Powers (Nazi Germany, Imperial Japan, Fascist Italy) and contemporary groups sharing similar ideology. 

 

Content that enables or promotes illegal activity, including but not limited to software piracy and copyright infringement, may be subject to moderation. Submissions must adhere to the licences and agreements associated with the modified or featured title(s). 

 

Files that are intended to destroy or otherwise cause harm to a user's person or property, such as viruses, malware, adware, etc., are prohibited. Submitted files will be subject to virus scanning and may be moderated as a result. 

 

Files that may be disruptive to any persistent multiplayer environment are prohibited. 

 

Files must be presented in good faith and in a functional state with respect to any caveats or requirements that you must detail and present to the user. Files that require registration or submission of user information in order to function, may only do so to facilitate the functions associated with the mod and must be clearly presented as such to the user. 

 

Files (especially executables) that connect to the internet to download or send information and/or files are prohibited unless where it is crucial for the functioning of the mod/utility. Please note that "auto update" functionality does not qualify as crucial and that we reserve the right to moderate and/or remove any files/tools/utilities/mods that communicate with the internet. If your tool's/app's functionality depends on the ability to send and receive information/files, please contact staff or send an email to supportwom@gmail.com laying out your reasoning and providing the source code for your tool/app. 

 

User submitted content may not be used to advertise other websites or services and may be removed at the discretion of staff  

 

Placeholders, password-protected, or otherwise wholly non-functioning files are prohibited. 

 

Using either files or images as a means of harassment of third parties is strictly prohibited  

 

Mods and/or descriptions containing adult content, such as swearing, extreme violence, nudity and/or sexual imagery need to be appropriately tagged/classified. 

 

File, image, and video submissions that feature content that would not adhere to the guidelines above on World Of Mods are prohibited. 

 

Images containing nudity are prohibited in the regular image share. The appropriate place for such imagery is in either the supporter image share or as part of an adult tagged mod if it serves to illustrate said mod’s functionality. 

 

Content related to Child Characters 

It is one of our core beliefs that modding can be a boon to your game experience and we believe that there is a place for mods improving upon the visuals of child characters in a decent and respectful manner. 

That being said, we understand that this is a sensitive matter that calls for clear guidelines as to what is and is not permissible when it comes to altering the appearance of child characters. 

Defining “Child Characters” 

A “child or child-like” character or NPC is defined as any character whose physical appearance or general characteristics gives sufficient reason to assume that they are a minor as defined by English law (under the age of 18). We identify this type of character with the following guidelines: 

Featuring bodily proportions that are predominantly associated with children. (examples include; small bodies, large heads and eyes, etc.) 

Having physical similarities to a child in height, build, features or mannerisms. 

Presented in a way that is indicative of a child in the base game. 

Canonically a child or child-like character in relation to the base game. 

Would be considered a child without game-specific context/lore defining the character (e.g. a childlike vampire established to be millennia old). 

Some examples of characters fitting these definitions: 

A regular child character. (e.g. child Link in Legend of Zelda: Ocarina of Time) 

A character who has had their physical characteristics frozen in time, but is described to be a lot older than they physically appear. (e.g. Babette in Skyrim, 200-year-old vampire in a child body). 

An older character who has been “de-aged” or had their consciousness transferred into a younger, child-like body. 

A character who appears to be an adult but is described and established as a child. 

A character whose listed age is that of an adult but their physical body resembles a child. 

A robot, android or other non-human, but anthropomorphised character with physical features indicative of human children (e.g. 10 year old Shaun from Fallout 4). 

What is and is not permissible when it comes to child mods? 

Reference point - vanilla game: 

Often times the developers of a given game have set the standards themselves by deliberately taking measures to ensure child characters are not able to be sexualised. This may include but is often not limited to: 

Irremovable clothes 

Irremovable underwear 

Underwear that is part of a given body mesh, or body texture (rather than an in-game item) 

Deliberate omission of reproductive organs or other sexual features (“Barbie/Ken” bodies) 

The inability for child characters to partake in any “romance” quest, plotline, or otherwise activity that can be perceived as romantic or sexual 

For the purpose of mods altering the appearance of child characters, it is, therefore, required that mod authors refrain from doing anything that would allow for measures such as those outlined above to be circumvented in one way or another: 

Clothes must remain irremovable through regular, in-game means 

Underwear must remain irremovable in the same way it would not be easily removable in the vanilla game without impacting the integrity of the body mesh/model and/or texture 

Any mod added child body or alteration must not feature reproductive organs or other sexual features (breasts) 

Any mod added child body model must not enhance sexual characteristics in a way that is typically associated with adult bodies 

Child characters must not be made available for “romance” or “marriage” quests or similar 

Child characters must not be given abilities or functionality that can be perceived as romantic or sexual 

Likewise, modded clothing attire for children should use the vanilla game as a reference point when it comes to determining the level of modesty that is appropriate in the given game context. 

Clothes for children should, in general, not be more revealing than would be considered appropriate for a child in a comparable real-world scenario 

Underwear (i.e. clothes that would be shown underneath a given default outfit) must not be substantially more revealing than the base game underwear to a degree that would imply sexualisation 

Note that while e.g. three-quarter trousers are technically more revealing than e.g. a turtleneck shirt in combination with a ball gown, it would still be considered modest on a child in a real-world scenario and would, therefore, be in compliance with our guidelines. 

 

Responsible conduct surrounding child character modifications 

Given the at times sensitive nature of the topic at hand, it is of paramount importance to remain respectful and tactful when discussing modifications of child characters and associated content: 

Mods featuring child characters should be appropriately named and refrain from using ambiguous attribution implying sexualisation or sexual connotation including but not limited to adjectives such as “sassy”, “naughty”, “sexy”, “(barely-)legal”, etc. 

Mod descriptions and associated content must not contain allusions to paraphiliae associated with illegal activity, nor hint at or openly discuss the (il)legality of child exploitation, grooming, etc. 

Discussing, hinting at, linking, or soliciting content that might be used in combination with a given child character mod in order to sexualise the child characters (e.g. external tools to add breast/butt physics) is strictly prohibited. 

At all times, we reserve the right to decide what we believe is and is not acceptable conduct surrounding the sensitive matter of child character modifications.   

 

Associated Content and Categorisation 

The following describes our guidelines when it comes to so-called Associated Content i.e. file names and descriptions: 

Files must be properly categorised. 

Files must be appropriately tagged according to the file’s content. Abuse of the tagging system and the addition of irrelevant tags is prohibited.   

File descriptions must be legible and informative regarding the nature of the file. Wholly misleading or incohesive descriptions are prohibited. 

Providing links to external sources for files that would not be permissible to be hosted on our services due to copyright violations or other is prohibited. 

When uploading images for files make sure they are relevant to the mod/game, don't mislead people and ensure no adult-only images are uploaded if your file is not an adult-only file. 

Public placeholders are prohibited. File pages without functioning files should remain unpublished, hidden, or be removed. 

We allow for a tactful discussion of donations and endorsements (such as thanking users for their contributions) in file descriptions. Actively soliciting either donations or endorsements, however, is prohibited and may be subject to moderation at the discretion of staff. For more information about Donations please contact worldofmods@gmail.com 

 

Distribution Permissions in Relation to other Users 

We provide users with the ability to detail how they wish their work to be reused within the World Of Mods network via our various permission preset options. Bear in mind that these permission settings are file specific. Alternatively, users are free to state their own terms regarding permissions for their content on a given file page. Only the original author of a given file or asset can specify its distribution permissions. 

If the author has not provided any permission information at all - you should assume that you must request permission from the author. 

Should any disputes occur within the context of our websites and services, these permissions will be referred to by staff who will attempt to enforce your wishes, within reason. If another World Of Mods user does not adhere to posted permission statements, their account may be subject to moderation based on review. 

If you give your permission to another author for them and/or others to use your work, then, pending a breach of whatever terms you decided, you cannot take back that permission once the other user(s)'s file has been released. 

Permissions and provisions you granted to other users of our site with regard to your content will remain in effect even if you cease to use World Of Mods or in the event that your account is terminated. 

Always document your conversations with other authors regarding permissions, preferably via private message on the World Of Mods, and provide staff with any pertinent evidence to facilitate investigation and conflict resolution when needed. 

Distribution Permissions in Relation to World Of Mods 

You only retain the rights to files you have uploaded to a World Of Mods site. You do not own the rights to files or content not posted by yourself that might have been created as a result of your content being available on any World Of Mods site, including, but not limited to, ratings, comments, images created by other users, articles in regards to your content and any statistical information in regards to your content. 

As a general rule, we do not remove user-submitted content from banned users, unless it is found in violation of our policies and guidelines. If your account is terminated by World Of Mods staff, you are free to decide how you want us to handle your uploaded content going forward by e.g. requesting its removal via worldofmods@gmail.com 

 

Console modding / content for platforms other than PC 

It is permissible to upload files/content to our services intended for usage on platforms other than PC provided that the following criteria are satisfied: 

The content is intended for use and can be used with an official version of the game on one or several of the official release platforms. Content that can only be used with an emulated version of the game/with an emulator is not permissible to be uploaded to our services. 

Files, software, or instructions regarding the "unlocking" of consoles ("jailbreaking") must not be distributed via our site and services. 

Noncompliance with the above may be met with the removal of any infringing content from our services as well as administrative action against your account. 

 

File Reporting and Moderation 

User submitted content that does not adhere to the standards described in this document may be subject to administration. 

You are encouraged to report content that may violate the standards described in this document.  Public accusations may be subject to moderation. 

If you encounter content that you find objectionable or no longer wish to see, please consider using our various blocking tools. Please, abstain from interacting with content that does not meet your subjective standards. Excessive disparagement of such content will result in administrative action against your account. 

If you have any comments, questions, or concerns,  contact worldofmods@gmail.com </p>
